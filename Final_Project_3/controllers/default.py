def get_images():
    """Gets the list of products, possibly in response to a query. (Add post)"""
    t = request.vars.q.strip()
    if request.vars.q:
        q = ((db.product.description.contains(t)))
    else:
        q = db.product.id > 0
    products = db(q).select(db.product.ALL)
    # Fixes some fields, to make it easy on the client side.
    for p in products:
        p.image_url = URL('default','download', args=p.image)
    return response.json(dict(
        products=products,
    ))


